#!/usr/bin/env bash
# Emit generated STATUS.md content for the consuming repo's mstack pipeline.
#
# Usage: pipeline-status.sh    (prints markdown to stdout;
#                               /mstack-status writes it to .mstack/STATUS.md)
#
# Scans: .mstack/plans/*.md, .mstack/reviews/<slug>.md,
#        .mstack/code/<slug>/tasks.md, .mstack/qa/*/report.md, TODOS file.

set -euo pipefail

ROOT="$(git rev-parse --show-toplevel 2>/dev/null || pwd)"
M="$ROOT/.mstack"

# field <file> <name> — value of a "**<name>:** value" line (first match)
field() { sed -n "s/^\*\*$2:\*\* *//p" "$1" 2>/dev/null | head -1; }

echo "# STATUS"
echo
echo "> Generated by /mstack-status on $(date +'%Y-%m-%d %H:%M'). Do not edit — regenerate instead."
echo

if [ ! -d "$M" ]; then
  echo "_No .mstack directory — run /mstack-init or /mstack-plan first._"
  exit 0
fi

echo "| Feature | Plan | Review | Code | Next |"
echo "|---|---|---|---|---|"
found=false
for plan in "$M"/plans/*.md; do
  [ -e "$plan" ] || continue
  found=true
  slug="$(basename "$plan" .md)"
  pstatus="$(field "$plan" Status)"; pstatus="${pstatus:-?}"

  review="$M/reviews/$slug.md"
  rstatus="—"
  [ -f "$review" ] && { rstatus="$(field "$review" Status)"; rstatus="${rstatus:-?}"; }

  ledger="$M/code/$slug/tasks.md"
  cstatus="—"
  if [ -f "$ledger" ]; then
    done_n="$(grep -c '^- \[x\]' "$ledger" || true)"
    total_n="$(grep -c '^- \[' "$ledger" || true)"
    lstatus="$(field "$ledger" Status)"; lstatus="${lstatus:-?}"
    cstatus="$lstatus ($done_n/$total_n)"
  fi

  case "$pstatus" in
    draft)       next="/mstack-review" ;;
    reviewed)    next="/mstack-code" ;;
    implemented) next="/mstack-qa, then /mstack-ship" ;;
    shipped)     next="—" ;;
    *)           next="?" ;;
  esac

  echo "| $slug | $pstatus | $rstatus | $cstatus | $next |"
done
if [ "$found" = false ]; then
  echo "| _none yet_ | | | | /mstack-plan |"
fi
echo

TODOS="$M/TODOS.md"
CFG="$M/config.json"
if [ -f "$CFG" ] && command -v jq >/dev/null 2>&1; then
  OVERRIDE="$(jq -r '.paths.todos // empty' "$CFG" 2>/dev/null || true)"
  [ -n "$OVERRIDE" ] && TODOS="$ROOT/$OVERRIDE"
fi
if [ -f "$TODOS" ]; then
  open_n="$(grep -c '^- \[ \]' "$TODOS" || true)"
  echo "**Open todos:** $open_n (see \`${TODOS#"$ROOT"/}\`)"
fi

latest_qa="$(find "$M/qa" -mindepth 1 -maxdepth 1 -type d 2>/dev/null | sort | tail -1)"
if [ -n "$latest_qa" ] && [ -f "$latest_qa/report.md" ]; then
  echo "**Latest QA:** $(basename "$latest_qa") — $(field "$latest_qa/report.md" Status)"
fi
